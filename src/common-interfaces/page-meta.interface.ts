/**
 * Page meta data
 */
export class PageMeta {
  /**
   * Page title
   */
  public title?: string;

  /**
   * Page description
   */
  public description?: string;

  /**
   * Page keywords
   */
  public keywords?: string;

  /**
   * Page url
   */
  public url?: string;

  /**
   * Page main image
   */
  public image?: string;

  /**
   * Page locale
   */
  public locale?: string;
}
